#include<stdio.h>

int main(){
    int a;
    a = 6;
    printf("The value of a is \n %d ", a); // \n is an escape character which starts a new line
    printf("The value of a is \t %d", a); // \t is an escape character which leaves blank spaces
    printf("Hey i am good how \'re you"); // We can use backslash and then inverted commas to indicate we are not ending print command but instead we want to use inverted comma in print.
    return 0;
}